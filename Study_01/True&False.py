# 참과 거짓
# 조건의 결과는 항상 참과 거짓이며 해당 자료는 논리형이다
print(True)  # True 와 False는 앞에 문자를 대문자로 사용한다
print(False)  # 문자열이 아니라 키워드라 따옴표가 필요 없다 키워드라 그대로 사용한다

# 값 비교하기
# ==(같다), !=(다르냐), <(크냐), <(작냐), >=(크거나 같냐), <==(작거나 같냐)
# 위 기호들은 약속이다
# 값을 비교하는 코드
print(1 < 2)
print(2 < 1)
print(2 > 1)
print(1 > 2)
print(1 <= 1)
print(2 <= 1)
print(1 >= 1)
print(1 >= 2)
print(1 == 1)
print(2 == 1)
print(1 != 2)
print(2 != 2)
